const defaultPrices = {
    single: 25000,
    double: 35000,
    triple: 50000,
    quad: 75000,
    family: 100000
};

function getRoomPrices() {
    try {
        const stored = localStorage.getItem('ataa-room-prices');
        return stored ? JSON.parse(stored) : defaultPrices;
    } catch (e) {
        return defaultPrices;
    }
}

function applyRoomPrices() {
    const prices = getRoomPrices();
    
    // Update translation strings dynamically for all languages
    translations.ar.price_single = `${prices.single.toLocaleString('en-US')} د.ع / ليلة`;
    translations.ar.price_double = `${prices.double.toLocaleString('en-US')} د.ع / ليلة`;
    translations.ar.price_triple = `${prices.triple.toLocaleString('en-US')} د.ع / ليلة`;
    translations.ar.price_quad = `${prices.quad.toLocaleString('en-US')} د.ع / ليلة`;
    translations.ar.price_family = `${prices.family.toLocaleString('en-US')} د.ع / ليلة`;
    
    translations.fa.price_single = `${prices.single.toLocaleString('fa-IR')} د.ع / شب`;
    translations.fa.price_double = `${prices.double.toLocaleString('fa-IR')} د.ع / شب`;
    translations.fa.price_triple = `${prices.triple.toLocaleString('fa-IR')} د.ع / شب`;
    translations.fa.price_quad = `${prices.quad.toLocaleString('fa-IR')} د.ع / شب`;
    translations.fa.price_family = `${prices.family.toLocaleString('fa-IR')} د.ع / شب`;
    
    translations.en.price_single = `${prices.single.toLocaleString('en-US')} IQD / night`;
    translations.en.price_double = `${prices.double.toLocaleString('en-US')} IQD / night`;
    translations.en.price_triple = `${prices.triple.toLocaleString('en-US')} IQD / night`;
    translations.en.price_quad = `${prices.quad.toLocaleString('en-US')} IQD / night`;
    translations.en.price_family = `${prices.family.toLocaleString('en-US')} IQD / night`;

    // Update select element options in index.html if present
    const roomSelect = document.getElementById('room-type');
    if (roomSelect) {
        const optionSingle = roomSelect.querySelector('[data-i18n-room="room_single"]');
        const optionDouble = roomSelect.querySelector('[data-i18n-room="room_double"]');
        const optionTriple = roomSelect.querySelector('[data-i18n-room="room_triple"]');
        const optionQuad = roomSelect.querySelector('[data-i18n-room="room_quad"]');
        const optionFamily = roomSelect.querySelector('[data-i18n-room="room_family"]');
        
        if (optionSingle) optionSingle.value = prices.single;
        if (optionDouble) optionDouble.value = prices.double;
        if (optionTriple) optionTriple.value = prices.triple;
        if (optionQuad) optionQuad.value = prices.quad;
        if (optionFamily) optionFamily.value = prices.family;
    }

    // Update select-room-btn data-price attributes in index.html if present
    document.querySelectorAll('.select-room-btn').forEach(btn => {
        const roomType = btn.getAttribute('data-room');
        if (roomType && prices[roomType]) {
            btn.setAttribute('data-price', prices[roomType]);
        }
    });
}

// Export functions to global scope
window.getRoomPrices = getRoomPrices;
window.applyRoomPrices = applyRoomPrices;
window.defaultPrices = defaultPrices;

// Run dynamic price application immediately
applyRoomPrices();

let currentLang = 'ar';
// Export for script.js
window.translations = translations;
window.currentLang = currentLang;

function switchLanguage(lang) {
    currentLang = lang;
    window.translations = translations; // Export for script.js
    window.currentLang = lang; // Export current language code for script.js
    
    // Always refresh and apply the room prices before performing the DOM translations updates
    applyRoomPrices();
    
    const t = translations[lang];
    if (!t) return;

    // Update direction
    document.documentElement.lang = lang === 'en' ? 'en' : lang;
    document.documentElement.dir = lang === 'en' ? 'ltr' : 'rtl';

    // Force date inputs to always use Gregorian calendar
    document.querySelectorAll('input[type="date"]').forEach(input => {
        input.setAttribute('lang', 'en');
    });

    // Update all data-i18n elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            el.textContent = t[key];
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            el.setAttribute('placeholder', t[key]);
        }
    });

    // Update room select options
    document.querySelectorAll('[data-i18n-room]').forEach(option => {
        const roomKey = option.getAttribute('data-i18n-room');
        const priceKey = option.getAttribute('data-i18n-price');
        if (t[roomKey] && t[priceKey]) {
            option.textContent = t[roomKey] + ' - ' + t[priceKey];
        }
    });

    // Update title
    if (t.title) document.title = t.title;

    // Update active lang button
    document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
    const activeBtn = document.getElementById('btn-lang-' + lang);
    if (activeBtn) activeBtn.classList.add('active');

    // Re-render reservations if exists
    if (typeof window.renderReservations === 'function') {
        window.renderReservations();
    }

    // Store preference
    localStorage.setItem('ataa-lang', lang);
}

// Initialize language from storage
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('ataa-lang');
    if (savedLang && translations[savedLang]) {
        switchLanguage(savedLang);
    } else {
        switchLanguage('ar');
    }
});
